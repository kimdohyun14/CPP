int main()
{
	MySimple<char, double> obj1;
	obj1.WhoAreYou();

	MySimple<int, long long> obj2;
	obj2.WhoAreYou();

	// 전체 특수화가 부분 특수화보다 우선시 된다.
	MySimple<int, double> obj3;
	obj3.WhoAreYou();
	return 0;
}
